# Function to clean and explode genre lists
def process_genres(df_column):
    # Ensure all entries are strings, replace non-strings with empty string
    cleaned_genres = df_column.astype(str).apply(lambda x: x.replace('na', '').replace('NA', '') if isinstance(x, str) else '')
    # Split by comma and space, then explode to individual genres
    individual_genres = cleaned_genres.str.split(', ').explode()
    # Remove any empty strings resulting from cleaning or splitting
    individual_genres = individual_genres[individual_genres != ''].str.strip()
    return individual_genres

